    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        //product search process
        try {
            // Database connection part
            Class.forName("com.mysql.jdbc.Driver");;// Load JDBC driver
            sqlconn = DriverManager.getConnection(dataconn, username, password);
            pst = sqlconn.prepareStatement("SELECT * FROM products WHERE productID = ?");
            
            // Set the parameters for the SQL statement
            pst.setString(1, productID.getText());
            
            rs = pst.executeQuery();
            if(rs.next() == false){
                JOptionPane.showMessageDialog(this, "Record Not Found!");
                productID.setText("");
                pName.setText("");
                categoryID.setText("");
                price.setText("");
                availability.setText("");
                content.setText("");
                description.setText("");
                
            }else{
                pName.setText(rs.getString("pName"));
                categoryID.setText(rs.getString("categoryID"));
                price.setText(rs.getString("price"));
                availability.setText(rs.getString("availability"));
                content.setText(rs.getString("content"));
                description.setText(rs.getString("description"));    
            }
            
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Record Not Found!" + e.getMessage());
        }
        
    }//GEN-LAST:event_jButton7ActionPerformed

