    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //save button for product save
            BigDecimal priceValue = new BigDecimal(price.getText());
            int contentValue = Integer.parseInt(content.getText());
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                sqlconn = DriverManager.getConnection(dataconn, username, password);
                pst = sqlconn.prepareStatement("INSERT INTO products(productID, pName, categoryID, price, availability, content, description) VALUES (?, ?, ?,?,?,?,?)");

                // Set the parameters for the SQL statement
                pst.setString(1, productID.getText());
                pst.setString(2, pName.getText());
                pst.setString(3, categoryID.getText());
                pst.setBigDecimal(4, priceValue);
                pst.setString(5, availability.getText());
                pst.setInt(6, contentValue);
                pst.setString(7, description.getText());

                pst.executeUpdate();

                JOptionPane.showMessageDialog(this, "Product added successfully!");
                
            } catch (Exception e) {
                
            JOptionPane.showMessageDialog(this, "Product added Unsuccessfully!" + e.getMessage());
            
            }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        //product update process
        BigDecimal priceValue = new BigDecimal(price.getText());
        int contentValue = Integer.parseInt(content.getText());
        
        try {
            
            Class.forName("com.mysql.jdbc.Driver");
            sqlconn = DriverManager.getConnection(dataconn, username, password);
            pst = sqlconn.prepareStatement("UPDATE products SET  pName=?, categoryID=?, price=?, availability=?, content=?, description=? WHERE productID = ?");
            
            // Set the parameters for the SQL statement
            pst.setString(1, pName.getText());
            pst.setString(2, categoryID.getText());
            pst.setBigDecimal(3, priceValue);
            pst.setString(4, availability.getText());
            pst.setInt(5, contentValue);
            pst.setString(6, description.getText());
            pst.setString(7, productID.getText());
            
            pst.executeUpdate();    
            
            JOptionPane.showMessageDialog(this, "Product Update Successfull!");
                
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Product Update Successfull!" + ex.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

